/**
 * Class that descripes simple adding and subtracting operations.
 * Will be used in app.js and is how the debugging screenshot will
 * be taken.
 * 
 * @author Liam Pickrell
 */


class MathOperations {

    add(a, b) {
        return a + b;
    }

    subtract(a, b) {
        return a - b;
    }
}

module.exports = MathOperations;